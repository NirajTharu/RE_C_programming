// How to use the different relational operators in c language
// == -> equal to;  "=" means asiging the vlaue to the variabe in the left side;
// >,>= -> greater than,greater than equal to ;
// <, <= -> less than, less than equal to;
// != -> not equal to;
// 1 is true  and 0 is false in the output of the program
#include<stdio.h>

int main()
{
    printf("%d\n",4==4);
    printf("%d\n",4>4);
    printf("%d\n",4>=4);
    printf("%d\n",4<4);
    printf("%d\n",4<=4);
    printf("%d\n",4!=4);
}